public class ReverseChars {
    public static void main(String[] args) {
        Scanner console = new Scanner(System.in);
        char c1 = console.next().charAt(0);
        char c2 = console.next().charAt(0);
        char c3 = console.next().charAt(0);

        System.out.print(c3);
        System.out.print(c2);
        System.out.print(c1);
    }
}
